package com.progen.games.simlperpg.gfx;

/**
 * The window context.
 * Provides info about the window.
 *
 */
public interface IWindow {

    /**
     * @return the width in pixels of the window.
     */
    int width();

    /**
     * @return the height in pixels of the window.
     */
    int height();

}
